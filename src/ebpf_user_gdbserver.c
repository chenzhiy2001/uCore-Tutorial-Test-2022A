void write_4bit(unsigned char n)
{
	if (n < 10 && n >= 0) {
		uart1_write(n + 48);
	} else if (10 <= n && n < 16) {
		uart1_write(n - 10 + 65);
	} else {
		printf("NOT 4bit!\n");
	}
}
void write_byte_as_hex(unsigned char b)
{
	write_4bit(b >> 4);
	write_4bit(b % 16);
}
int write_packet(unsigned char *buf, int len)
{
	uart1_write('$');
	for (int i = 0; i < len; i++) {
		uart1_write(buf[i]);
	}
	uart1_write('#');
	unsigned char checksum;
	size_t i;
	for (i = 0, checksum = 0; i < len; ++i)
		checksum += buf[i];
	write_byte_as_hex(checksum);
}
unsigned char packet_buffer[PACKET_BUFFER_SIZE] = { 0 };
void print_packet_buffer()
{
	printf("Full Packet data (in a disgusting form because printf doesn't support %c):\n");
	for (size_t i = 0; i < PACKET_BUFFER_SIZE; i++) {
		printf("%x ", packet_buffer[i]);
	}
}
void flush_packet_buffer()
{
	for (size_t i = 0; i < PACKET_BUFFER_SIZE; i++) {
		packet_buffer[i] = 0;
	}
}
void read_packet()
{
	flush_packet_buffer();
	size_t end_count = PACKET_BUFFER_SIZE;
	for (size_t i = 0; end_count; i++, end_count--) {
		packet_buffer[i] = uart1_read();
		if (packet_buffer[i] == '#') {
			end_count = 3;
		}
	}
	uart1_write('+');
	printf("Received Packet:\n%s\n", packet_buffer);
}
void process_packet()
{
	int daoler = -1; //dollar
	for (size_t i = 0; i < PACKET_BUFFER_SIZE; i++) {
		if (packet_buffer[i] == '$')
			daoler = i;
	}
	if (packet_buffer[daoler + 1] == '?') {
		write_packet("S05", 3);
	} else if (packet_buffer[daoler + 1] == 'g') {
		print_packet_buffer();
		uart1_write('$');
		for (size_t i = 0; i < 512 + 16; i++) {
			uart1_write('0');
		}
		uart1_write('#');
		uart1_write('0');
		uart1_write('0');

	} else if (packet_buffer[daoler + 1] == 'm') {
		printf("MMM command");
	} else if (packet_buffer[daoler + 1] == 'q') {
		write_packet("", 0);
		//write_packet(stub_features, strlen(stub_features));
	} else if (packet_buffer[daoler + 1] == 'v') {
			if (packet_buffer[daoler + 2] == 'T' &&
				packet_buffer[daoler + 3] ==
					'R') { // vTR = v_Tracepoint_then_get_Registers
				printf("Detected Kernel Regs Request\n");
				int packet_content_start = daoler + 4;
				const char *ty_type = "kprobe$";

				unsigned *p = (unsigned *)kern_get_regs;
				int prog_size = KERN_GET_REGS_PROG_SIZE; //hardcoded. this is so stupid
				struct bpf_map_fd_entry map_array[] = {}; // empty
				int bpf_fd =
					bpf_prog_load_ex(p, prog_size, map_array, 0);

				char target[TARGET_STR_SIZE] = {
					'\0'
				}; //so many \0. correct?

				for (int i = 0; i < strlen(ty_type); i++) {
					target[i] = ty_type[i];
				}

				for (int i = 0; i < TARGET_STR_SIZE - strlen(ty_type);
			     i++) {
				if (packet_buffer[packet_content_start + i] ==
				    '#') {
					target[strlen(ty_type) + i] = '\0';
					break;
				}
				if (packet_buffer[packet_content_start + i] ==
				    ':') {
					target[strlen(ty_type) + i] = '$';
				} else {
					target[strlen(ty_type) +
					       i] = packet_buffer
						[packet_content_start + i];
					}
				}


				//const char *target = "kprobe$<linux_syscall::Syscall>::sys_openat"; //todo: any str
				uint32_t str_len = strlen(target);
				bpf_prog_attach(target, str_len, bpf_fd);
				write_packet("OK", 2);
				exit(0);

			} else if (packet_buffer[daoler + 2] == 'T' &&
				packet_buffer[daoler + 3] ==
					'U') // vTU = v_Tracepoint_then_get_User_registers
		{
			printf("Detected User Regs Request\n");
			
			int packet_content_start = daoler + 4;
			const char *ty_type = "uprobe_syncfunc$";
			unsigned *p = (unsigned *)user_get_regs;
			int prog_size = USER_GET_REGS_PROG_SIZE; //hardcoded. this is so stupid
			struct bpf_map_fd_entry map_array[] = {}; // empty
			int bpf_fd =
				bpf_prog_load_ex(p, prog_size, map_array, 0);
			char target[TARGET_STR_SIZE] = {
				'\0'
			}; //so many \0. correct?
			for (int i = 0; i < strlen(ty_type); i++) {
				target[i] = ty_type[i];
			}
			for (int i = 0; i < TARGET_STR_SIZE - strlen(ty_type);
			     i++) {
				if (packet_buffer[packet_content_start + i] ==
				    '#') {
					target[strlen(ty_type) + i] = '\0';
					break;
				}
				if (packet_buffer[packet_content_start + i] ==
				    ':') {
					target[strlen(ty_type) + i] = '$';
				} else {
					target[strlen(ty_type) +
					       i] = packet_buffer
						[packet_content_start + i];
				}
			}
			//const char *target = "kprobe$<linux_syscall::Syscall>::sys_openat"; //todo: any str
			uint32_t str_len = strlen(target);
			bpf_prog_attach(target, str_len, bpf_fd);
			write_packet("OK", 2);
			exit(0); //because the shell doesn't have "CTRL+Z and fg" functionalities, we have to quit gdbserver directly.
		}
	}
	else if (packet_buffer[daoler + 1] == 'H')
	{
		write_packet("", 0);
		// if(packet_buffer[daoler + 2] == 'g'){
		//     write_packet("OK", 2);
		// }
	}
	else
	{
		printf("Unsupported command %d\n", packet_buffer[daoler + 1]);
		print_packet_buffer();
		write_packet("", 0);
	}
}
